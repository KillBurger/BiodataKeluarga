public class Saya {
    private String nama, alt, tlhr, tgl, hobi, ct, no;

    public String getNama() {
        return nama;
    }

    public String getAlt() {
        return alt;
    }

    public String getTlhr() {
        return tlhr;
    }

    public String getTgl() {
        return tgl;
    }

    public String getHobi() {
        return hobi;
    }

    public String getCt() {
        return ct;
    }

    public String getNo() {
        return no;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public void setAlt(String alt) {
        this.alt = alt;
    }

    public void setTlhr(String tlhr) {
        this.tlhr = tlhr;
    }

    public void setTgl(String tgl) {
        this.tgl = tgl;
    }

    public void setHobi(String hobi) {
        this.hobi = hobi;
    }

    public void setCt(String ct) {
        this.ct = ct;
    }

    public void setNo(String no) {
        this.no = no;
    }
    
    
}
